package com.srinu.abstraction;

public  abstract class DemoAbstract {
	
	public  abstract void deposit();
	
	
	
	// we can write constructor for  the abstract class
	// we can create static block
	static{
		
		System.out.println("static block in  abstract class");
	}
	// we can create instance block in abstract class in abstract
	{
		System.out.println("instance in abbstract");
	}
	
	public void demo()
	{
		System.out.println("instacne method in abstract class");
	}
	// we can create constructor in abstract class
	DemoAbstract()
	{
		System.out.println("contructor in abstract clas");
	}
	// we  can   create  main  to the abstract class
	// we cannot instantiate the abstract class
	
	

}
